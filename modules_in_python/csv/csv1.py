# CSV (Comma Separated Values)
# É um formato de arquivo que armazena dados em forma de tabela,
# onde cada linha representa uma linha da tabela e as colunas são separadas por virgulas.
# Ele é amplamente utilizado para transferir dados entre sistemas de diferentes plataformas,
# como por exemplo, para importar ou exportar dados para uma planilha
# (Google Sheets, Excel, libreOffice Calc)
# Um arquivo CSV geralmente tem a extensão ".csv" e pode ser aberto em um editor de texto ou em uma planilha eletronica
# Um exemplo de um arquibo CSV pode ser:
# Nome, idade, Endereço
# Luiz Otávio, 32, "AV Brasil, 21, Centro"
# João da Silva,55, "Ria 22,44, NOva era"
# A primeira linha do arquivo define os nomes das colunas da, enquanto as
# Linhas seguintes contêm os vcalores das linhas, separados por virgulas
# Regras simples do CSV
# 1 - Separe os valores das colunas com um delimitador único (,)
# 2 - Cada registro deve estar em uma linha
# 3 - Não deixar linhas ou espaços sobrando
# 4 - Use o caractere de escape (") quando o delimitador aparecer no valor.
